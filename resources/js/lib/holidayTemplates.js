export function findMatchingHoliday(holidays, item) {
    if (!item) {
        return null;
    }

    return holidays.find((holiday) =>
        (holiday.value.mon ?? '') === (item.mon ?? '')
        && (holiday.value.mday ?? '') === (item.mday ?? '')
        && (holiday.value.wday ?? '') === (item.wday ?? '')
        && (holiday.value.mweek ?? '') === (item.mweek ?? '')
    ) ?? null;
}

export function findMatchingHolidayLabel(holidays, value) {
    const match = holidays.find((holiday) =>
        holiday.value.mon === value.value.mon
        && holiday.value.mday === value.value.mday
        && holiday.value.mweek === value.value.mweek
        && holiday.value.wday === value.value.wday
    );

    return match?.label ?? '';
}

export function handleTemplateHolidayUpdate(holidays, newValue, oldValue, el$) {
    if (newValue === oldValue) {
        return;
    }

    el$.form$.update({
        mday: newValue.value.mday,
        mon: newValue.value.mon,
        mweek: newValue.value.mweek,
        wday: newValue.value.wday,
        description: findMatchingHolidayLabel(holidays, newValue),
    });
}

export function stripTemplateHolidayFields(requestData) {
    holidayTemplateCountries.forEach((country) => {
        delete requestData[country.fieldName];
    });
}

export function buildTemplateHolidayDefaults(item) {
    return Object.fromEntries(
        holidayTemplateCountries.map((country) => [
            country.fieldName,
            findMatchingHoliday(country.holidays, item),
        ])
    );
}
